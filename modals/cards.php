    public function getBairro($documento){
        $Crud = new Crud('barbearia','root', '');
        $Crud->SetBancoDeDados();
        $conexão = $Crud->conectar();
        $query = "SELECT bairro FROM salao WHERE documento = '$documento'";
        $result = $conexão->query($query);
        $row = $result->fetchAll();
        $bairro = $row[0]['bairro'];
        return $bairro;
    }

    public function constructCardsPage(){
        $Crud = new Crud('barbearia', 'root', '');
        $Crud->SetBancoDeDados();
        $conexão = $Crud->conectar();
        $query = $conexão->query("SELECT * FROM card");
        $result = $query->fetchAll();
        foreach($result as $row){
            $Documento = $row['documentoSalao_card'];
            $Bairro = $this->getBairro($Documento);
            $Foto = $row['foto'];
            $Nome = $row['nome'];
            $Descricao = $row['descricao'];
            $Preco = $row['preco'];
            $Star = $row['star'];
            $Especializacao = $row['especializacao'];
            $this->Card($Documento,$Foto,$Nome,$Descricao,$Preco,$Bairro,$Star,$Especializacao);
        }
        

    }

